package com.ssafy.happyhouse.model;

public class EnvDto {
	private String facName;
	private String jobCode;
	private String jobName;
	private String mapCheckDate;
	private String checkAg;
	private String checkAgName;
	private String checkDesc;
	private String roadAddress;

	public String getFacName() {
		return facName;
	}

	public void setFacName(String facName) {
		this.facName = facName;
	}

	public String getJobCode() {
		return jobCode;
	}

	public void setJobCode(String jobCode) {
		this.jobCode = jobCode;
	}

	public String getJobName() {
		return jobName;
	}

	public void setJobName(String jobName) {
		this.jobName = jobName;
	}

	public String getMapCheckDate() {
		return mapCheckDate;
	}

	public void setMapCheckDate(String mapCheckDate) {
		this.mapCheckDate = mapCheckDate;
	}

	public String getCheckAg() {
		return checkAg;
	}

	public void setCheckAg(String checkAg) {
		this.checkAg = checkAg;
	}

	public String getCheckAgName() {
		return checkAgName;
	}

	public void setCheckAgName(String checkAgName) {
		this.checkAgName = checkAgName;
	}

	public String getCheckDesc() {
		return checkDesc;
	}

	public void setCheckDesc(String checkDesc) {
		this.checkDesc = checkDesc;
	}

	public String getRoadAddress() {
		return roadAddress;
	}

	public void setRoadAddress(String roadAddress) {
		this.roadAddress = roadAddress;
	}

}
